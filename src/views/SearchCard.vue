<template>
  <div>
    <v-form @submit.prevent="search">
      <v-container>
        <v-row align="center"
        class="text-center">
            <v-col cols="12" sm="9" md="10">
                <v-text-field
                v-model="query"
                label="Search a card here"/>
            </v-col>
            <v-col cols="12" sm="3" md="2">
              <v-btn color="primary"
              @click="search">Search !</v-btn>
            </v-col>
        </v-row>
      </v-container>
    </v-form>
    <v-container>
      <v-row
      v-if="!cards.length && queried"
      align-content="center"
      class="text-center">
        <v-col cols="12">
          <h1>No results.</h1>
        </v-col>
        <v-col cols="12">
          <h1>¯\_(ツ)_/¯</h1>
        </v-col>
      </v-row>

      <v-row
      v-if="loading"
      class="text-center">
        <v-col cols="12">
          <v-progress-circular
          color="primary"
          indeterminate
          size="128"/>
          <v-col cols="12">
            <h1>The magic is loading…</h1>
          </v-col>
          <v-col cols="12">
            <h2>(it usually take a little time)</h2>
          </v-col>
        </v-col>
      </v-row>

      <v-row>
        <Card
          v-for="card in cards"
          :key="card.id"
          :card="card"/>
      </v-row>
    </v-container>
  </div>
</template>

<script>
import Card from '@/components/Card.vue'
const mtg = require('mtgsdk')

export default {
  name: 'SearchCard',

  data: () => ({
    cards: [],
    query: '',
    queried: false,
    loading: false
  }),
  methods: {
    async search () {
      this.loading = true
      mtg.card.where({ name: this.query })
        .then(cards => {
          this.loading = false
          this.queried = true
          this.cards = cards
        })
        .catch(error => error)
    }
  },
  components: {
    Card
  }
}
</script>
